import { access, mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");
const fixturesDir = path.join(projectRoot, "src", "fixtures");
const generatedDir = path.join(fixturesDir, "generated");

const dictionarySources = [
  path.join(fixturesDir, "korean_dictionary1.json"),
  path.join(fixturesDir, "korean_dictionary2.json"),
];

const dictionaryOutputPath = path.join(
  generatedDir,
  "koreanDictionaryWords.generated.ts",
);
const additionalWordsSourcePath = path.join(fixturesDir, "끄코_단어목록.txt");
const additionalWordsOutputPath = path.join(
  generatedDir,
  "additionalWordList.generated.ts",
);
const WORD_PATTERN = /^\{'word':\s*'((?:\\'|[^'])*)',\s*'raw':/;

function renderStringArrayExport(exportName, words) {
  return [
    "// Generated by scripts/generate-word-fixtures.mjs. Do not edit manually.",
    `export const ${exportName} = [`,
    ...words.map((word) => `  ${JSON.stringify(word)},`),
    "];",
    "",
  ].join("\n");
}

function parseDictionaryWords(raw) {
  const words = [];

  for (const line of raw.split(/\r?\n/)) {
    const match = line.trim().match(WORD_PATTERN);

    if (match == null) {
      continue;
    }

    const word = match[1].replace(/\\'/g, "'").trim();

    if (word !== "") {
      words.push(word);
    }
  }

  return words;
}

function parsePlainWordList(raw) {
  const words = [];

  for (const line of raw.split(/\r?\n/)) {
    const word = line.trim();

    if (word !== "") {
      words.push(word);
    }
  }

  return words;
}

async function fileExists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  await mkdir(generatedDir, { recursive: true });

  const dictionaryWords = new Set();

  for (const sourcePath of dictionarySources) {
    if (!(await fileExists(sourcePath))) {
      continue;
    }

    const raw = await readFile(sourcePath, "utf8");

    for (const word of parseDictionaryWords(raw)) {
      dictionaryWords.add(word);
    }
  }

  if (dictionaryWords.size > 0) {
    await writeFile(
      dictionaryOutputPath,
      renderStringArrayExport(
        "KOREAN_DICTIONARY_WORD_LIST",
        Array.from(dictionaryWords),
      ),
      "utf8",
    );
  }

  if (await fileExists(additionalWordsSourcePath)) {
    const raw = await readFile(additionalWordsSourcePath, "utf8");
    const additionalWords = Array.from(new Set(parsePlainWordList(raw)));

    await writeFile(
      additionalWordsOutputPath,
      renderStringArrayExport("ADDITIONAL_WORD_LIST_DATA", additionalWords),
      "utf8",
    );
  }
}

await main();
